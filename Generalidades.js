function Mostrar() {
    document.getElementById("RespuestaUno").style.display = "block";
}
function Ocultar() {
    document.getElementById("RespuestaUno").style.display = "none";
}
function Mostrar_Ocultar() {
    var RespuestaUno = document.getElementById("RespuestaUno");
    if (RespuestaUno.style.display == "none") {
        Mostrar();
        document.getElementById("Boton").value = "Ocultar";
    }
    else {
        Ocultar();
        document.getElementById("Boton").value = "Mostrar";
    }
}

